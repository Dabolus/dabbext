func braille(text string) string {
	var ret bytes.Buffer

	for _, letter := range text {
		if val, ok := brailleTable[letter]; ok {
			ret.WriteString(val)
		} else {
			ret.WriteRune(letter)
		}
	}

	return ret.String()
}
